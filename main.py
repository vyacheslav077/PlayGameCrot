        self.lbl_lives.pack(side="right", padx=18)
        # Жизни справа. side="right" -прижать к правому краю.
 
        self.lbl_status = tk.Label(self.game_win, text="",
                                    font=("Segoe UI", 12, "italic"), fg=SUBTEXT, bg=BG)
        self.lbl_status.pack(pady=(14, 0)) #lbl_status — это Label с текстом статуса 
        # Статусная строка под HUD. Показывает: "найди крота", "Попадание!", "Промах!".
        # italic -курсив для визуального отличия от заголовков.
 
