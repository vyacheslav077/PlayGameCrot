    def start(self):# Вызывается при нажатии "Начать игру" или Enter.
 
        self.player = self.entry.get().strip() or "Аноним"
        # self.entry.get()-читает текст из поля ввода. entry — поле ввода которое мы создали, .get() — метод который читает что сейчас написано внутри и возвращает строку
