        StyledButton(self.game_win, "В меню", self.back,
                     bg=BTN_BG, hover=BTN_HVR, fg=SUBTEXT,
                     font_size=11, pad_x=20, pad_y=7).pack(pady=(0, 18))
        # Кнопка возврата в меню внизу окна.
 
        self.load_level()
