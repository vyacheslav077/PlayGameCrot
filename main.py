        # "WM_DELETE_WINDOW"  событие нажатия кнопки "×" (закрыть).
        # По умолчанию: просто закрывает окно.
        # Мы переопределяем: вместо закрытия => вызвать self.back().
        # back() корректно возвращает в меню, а не просто уничтожает окно.
 
