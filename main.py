        # or "Аноним" -если строка пустая
 
        self.lives = self.MAX_LIVES # 6 жизней в начале. 
        self.level = 0 # Индекс уровня: 0 = первый (LEVELS[0])
        self.start_time = time.time() # Запоминаем момент старта
        # time.time() = число секунд с 1970-01-01. Например: 1710329521.847
        # В конце игры: time.time() - self.start_time = секунды игры.
 
        log(f"Игрок '{self.player}' начал игру")  # Запись в лог. Видно в консоли и в logs/game_log.txt.
 
        self._build_game_window() # Строим и показываем игровое окно.

 
