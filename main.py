        self.game_win = tk.Toplevel(self.root)
        # Toplevel — дополнительное окно. Привязано к self.root:
        # если закрыть root, Toplevel тоже закроется.
        # Главное отличие от Tk(): Toplevel не создаёт новый mainloop.
 
        self.game_win.title("Крот-Охотник — Игра")
        self.game_win.configure(bg=BG)
        self.game_win.resizable(False, False)
 
