#  КЛАСС StyledButton - кастомная кнопка на базе tk.Label нужен для отображения текста или изображений внутри окна приложения.

 
class StyledButton(tk.Label): #tk.Label - виджет для отображения текста или изображений внутри
    # Наследуемся от tk.Label (если .buton системн винды и трудно убрать, а label - просто прямоугольник, полностью управляемый нами)
    # tk.Label полностью управляется нашими настройками - можно задать любой цвет, убрать рамку, сделать hover-эффект через bind
 
    def __init__(self, parent, text, command,
                 bg=BTN_BG, hover=BTN_HVR, fg=TEXT,
                 font_size=13, bold=False, pad_x=24, pad_y=10, **kwargs):
        # __init__ - конструктор, вызывается при создании объекта. self - как ссылка на конкретный экземпляр
        # parent  - родительский виджет (окно или фрейм). Каждый виджет в tkinter создаётся внутри другого виджета. parent — это тот виджет, внутри которого создаётся текущий
        # text    - надпись на кнопке. Обязательный
        # command - функция которую вызвать при клике.
        # bg      - цвет фона. По умолчанию BTN_BG
        # hover   - цвет фона при наведении. По умолчанию BTN_HVR
        # fg      - цвет текста. По умолчанию TEXT (почти белый)
        # font_size - размер шрифта в пунктах. По умолчанию 13
        # bold    - жирный шрифт. По умолчанию False
        # pad_x/pad_y - внутренние отступы (увеличивают размер кнопки)
        # **kwargs - любые доп. параметры tk.Label (например anchor)
 
        weight = "bold" if bold else "normal"
        # Тернарный оператор: если bold=True => weight="bold", иначе => "normal".
        # tkinter шрифт принимает строку "bold" или "normal", не True/False.
 
        super().__init__(
            parent,
            text=text,
            font=("Segoe UI", font_size, weight), # Шрифт задаётся кортежем: (название, размер, стиль). "Segoe UI" - стандартный шрифт Windows, выглядит красиво.
            bg=bg, fg=fg, #Левая сторона (bg=) — параметр tkinter Label.Правая сторона (=bg) — переменная из аргументов функции.То есть: "передай в Label тот цвет который передали нам".
            cursor="hand2",# cursor="hand2" - курсор-рука при наведении. Без этого был бы обычный курсор-стрелка.
            relief="flat", bd=0, # relief="flat" - убирает объёмный вид (нет эффекта "нажатой кнопки"). bd=0 - убирает рамку (border width = 0 пикселей).
            padx=pad_x, pady=pad_y, # Внутренние отступы между текстом и краем кнопки. Без них текст был бы прижат к краям и кнопка выглядела бы маленькой.
            # Внутренние отступы между текстом и краем кнопки.
            # padx=40, pady=12 делает кнопку шире и выше.
            **kwargs, # Передаём все лишние параметры в tk.Label. Например если написать StyledButton(..., anchor="w") - текст выровняется по левому краю.
        )
 
        self._bg = bg # Сохраняем исходный цвет фона
        self._hover = hover # Сохраняем hover-цвет. используется для реализации эффектов, возникающих при наведении курсора мыши на элемент интерфейса
        self._cmd = command # Сохраняем функцию-команду
        # Чтобы использовать в методах _click, bind.
        # Без self. переменная была бы локальной и исчезла бы после __init__.
 
        self.bind("<Enter>", lambda _: self.config(bg=self._hover)) 
        # bind - подписка на событие tkinter. При наступлении события вызывается функция-обработчик. В данном случае:
        # "<Enter>" -событие: курсор мыши вошёл на виджет.
        # lambda _ - нонимная функция, _ означает "аргумент есть, но не нужен"
        # self.config(bg=...) - меняет цвет фона на hover.
        # Результат: кнопка светлеет при наведении мыши.
 
        self.bind("<Leave>", lambda _: self.config(bg=self._bg))
        # "<Leave>" - курсор покинул виджет.
        # Возвращаем исходный цвет фона.
        # Без этого кнопка навсегда осталась бы в hover-состоянии.
 
        self.bind("<Button-1>", self._click)
        # "<Button-1>" - нажата левая кнопка мыши (Button-2 = средняя, Button-3 = правая).
        # Вызывает метод self._click при клике.
 
    def _click(self, _):  # Вызывается при клике. _ = объект события (не используем).
 
        self.config(bg=ACCENT2)
        # Мгновенно меняем цвет на светло-фиолетовый - изуальная "вспышка" клика.
 
        self.after(110, lambda: self.config(bg=self._hover)) #self.after(110, ...) - запускает функцию через 110 миллисекунд. В данном случае -меняет
        # after(задержка_мс, функция) - апустить функцию через N миллисекунд.
        # Через 110мс возвращаем hover-цвет - митация "отпускания" кнопки.
        # Вместе с первой строкой создаёт анимацию: вспышка => затухание.
 
        if self._cmd:
            # Проверяем что команда существует (не None).
            # Позволяет создать "заглушку" кнопки без команды: StyledButton(..., command=None)
            self.after(130, self._cmd)
            # Вызываем команду через 130мс -чуть позже анимации.
            # Если вызвать сразу-команда выполнится ДО того как анимация покажется.
 
 

# КЛАСС HoleButton -кнопка-лунка, нарисованная на холсте Canvas
 
class HoleButton(tk.Canvas): # Наследуемся от tk.Canvas- холст для рисования фигур, линий, текста. На Canvas мы рисуем круги вручную и получаем лунку любого вида.
 
    SIZE = 130 # Переменная КЛАССА (не экземпляра) - одинакова для всех лунок. Размер холста: 130×130 пикселей.
    # Доступна как HoleButton.SIZE или self.SIZE.
 
    def __init__(self, parent, row, col, on_click):# parent  - родительский фрейм (self.field в Game). 
        #row, col -позиция лунки в сетке (0-based индексы)
        # on_click - функция Game.hit(r, c) которую вызвать при клике. "hit" — попадание (зелёный) или "miss" — промах (красный). Передаётся из Game при создании лунок, сохраняется в self._on_click и вызывается при клике на лунку.
 
        s = self.SIZE  # s = 130, короткое имя для удобства в расчётах
 
        try:
            pbg = parent.cget("bg")
            # cget("bg") - получить текущий цвет фона родителя. Нужно чтобы Canvas "сливался" с фоном и не было видно
            # прямоугольного края холста вокруг круглой лунки.
        except Exception:
            pbg = BG
            # Если по какой-то причине cget не сработал - используем глобальный цвет фона (BG)
 
        super().__init__(parent,
                         width=s, height=s,
                         bg=pbg,
                         highlightthickness=0, bd=0,
                         cursor="hand2")
        # Создаём Canvas размером 130×130.
        # highlightthickness=0 убирает синюю рамку фокуса (появляется при клике).
        # bd=0 -убирает внешнюю рамку холста.
        # Без этих двух параметров вокруг каждой лунки была бы некрасивая рамка.
 
        self._s = s #s - локальная переменная внутри __init__: self._s = s. Она сохраняет размер холста (130) для использования в других методах класса, например в _draw(). Без self._s пришлось бы каждый раз писать self.size, что длиннее.
        self._row = row
        self._col = col
        self._on_click = on_click
        self._state = "idle" #Маленький кружок размером 12×12 пикселей
        # Состояние лунки
        # "idle" -обычное, ожидает клика
        # "hit"  -попадание (зелёная)
        # "miss" - промах (красная)
        # Используется в _draw() для выбора внешнего вида. Метод который перерисовывает лунку с нуля при каждом изменении состояния.
 
        self._hover = False # hover -наведён ли курсор на лунку. Изначально False, потому что курсор изначально не над лункой, а над ней появляется только при наведении мыши.
        # True когда курсор находится над лункой.
        # Используется в _draw() для подсветки обводки.
 
        self.after(0, self._draw)  # self.after(0, ...) запускает _draw через 0мс, после того как Python полностью создаст и зарегистрирует виджет.
        # Если вызвать self._draw() прямо здесь- Canvas ещё не готов, self.delete("all") упадёт с ошибкой "invalid command name".
 
        self.bind("<Enter>",    self._enter) # Подписка на события мыши. Когда курсор входит в область холста -вызывается self._enter, когда покидает - self._leave, при клике - self._click.
        self.bind("<Leave>",    self._leave) # Без этих подписок лунка не реагировала бы на наведение мыши и не показывала бы подсветку.
        self.bind("<Button-1>", self._click) # При клике вызывается self._click, который в свою очередь вызывает функцию on_click из Game.hit(row, col) и меняет состояние лунки на "hit" или "miss".
        # Подписка на события мыши -аналогично StyledButton.
 
    def _draw(self): # Перерисовывает лунку с нуля. Вызывается при первой отрисовке (через after(0, ...))
        # - hover (вход/выход мыши)
        # - смене состояния (hit/miss), попадание или промах
 
        self.delete("all") #
        # Удаляем ВСЁ что было нарисовано на холсте. Без этого каждый вызов _draw рисовал бы поверх предыдущего 
 
        s   = self._s # 130 - размер холста, сохранённый в __init__. Короткое имя для удобства.
        pad = 10 # Отступ от края холста до лунки (в пикселях)
 
        # ТЕНЬ
        self.create_oval(pad+5, pad+5, s-pad+5, s-pad+5,
                         fill="#07070f", outline="")
        # Рисуем тёмный круг смещённый на +5px вправо и вниз.
        # Координаты create_oval: (x1, y1, x2, y2) -углы описывающего прямоугольника.
        # При s=130, pad=10: (15, 15, 125, 125) -круг диаметром 110px.
        # fill="#07070f" -почти чёрный. цвет заливки внутри круга
        # outline="" - без обводки.  # цвет обводки (контура) круга
        # Смещение +5 создаёт эффект тени и иллюзию объёма лунки.
 
        # ЦВЕТ ОБВОДКИ 
        if self._state == "hit":
            rim = GREEN    # Зелёная обводка при попадании
        elif self._state == "miss":
            rim = RED      # Красная обводка при промахе
        else:
            rim = ACCENT2 if self._hover else HOLE_RIM
            # При наведении -светло-фиолетовая подсветка (подсказка что можно кликнуть)
            # В обычном состоянии -тёмно-синяя обводка
 
        # ОСНОВНАЯ ЛУНКА 
        self.create_oval(pad, pad, s-pad, s-pad, # size=130, pad=10 => от (10,10) до (120,120) -круг диаметром 110px. Координаты задают прямоугольник в который вписывается круг.
                         fill=HOLE, outline=rim, width=3)
        # При s=130, pad=10: круг от (10,10) до (120,120) диаметр 110px.
        # fill=HOLE -тёмно-синяя заливка.
        # outline=rim -цвет обводки выбран выше.
        # width=3 -толщина обводки 3 пикселя.
 
        # СОДЕРЖИМОЕ В ЗАВИСИМОСТИ ОТ СОСТОЯНИЯ
        if self._state == "hit":
            inner = pad + 16   # 26 pad=10 --> большой круг (лунка), inner=26 -> маленький круг (зелёный/красный)
            self.create_oval(inner, inner, s-inner, s-inner, #create_oval принимает 4 координаты — это левый верхний и правый нижний угол воображаемого прямоугольника, в который вписывается круг
                             fill="#065F46", outline=GREEN, width=2)
            # Рисуем внутренний зелёный круг -"дно" лунки где нашли крота.
            # Меньше основного: от (26,26) до (104,104).
 
            self.create_text(s//2, s//2, text="W", #
                             font=("Consolas", 28, "bold"), fill=GREEN)
            # Текст "W" (Win) в центре. s//2 = 65 -центр холста.
            # Consolas -моноширинный шрифт, буква получается широкой и чёткой.
 
        elif self._state == "miss": # аналогично для промаха, но красный цвет и текст "X" (miss)
            inner = pad + 16
            self.create_oval(inner, inner, s-inner, s-inner, #Рисует текст в координатах (65, 65) — то есть ровно по центру холста. Первые два аргумента — это x и y точки где будет центр текста.
                             fill="#450A0A", outline=RED, width=2)
            # Аналогично hit, но тёмно-красная заливка.
 
            self.create_text(s//2, s//2, text="X", #s//2 = 65 -центр холста. Текст "X" (miss) в центре.
                             font=("Consolas", 28, "bold"), fill=RED)
            # "X" в центре -промах.
 
        else: # иначе -обычное состояние, лунка не нажата
            c = s // 2   # 65 -центр холста. s = SIZE = 130 — размер холста в пикселях
            r = 6        # радиус точки 6 пикселей
            self.create_oval(c-r, c-r, c+r, c+r,
                             fill=HOLE_RIM, outline="")
            # Декоративная точка: от (59,59) до (71,71)
            # Создаёт ощущение глубины лунки
 
    def _enter(self, _):
        # Курсор вошёл на холст.
        if self._state == "idle": # idle -обычное состояние: маленькая точка в центре
            # Меняем hover ТОЛЬКО если лунка ещё не нажата
            # Если уже "hit" или "miss" -подсветка не нужна
            self._hover = True
            self._draw()   # Перерисовываем с новой обводкой ACCENT2
 
    def _leave(self, _): # создаем метод для обработки события покидания курсором холста
        # Курсор покинул холст
        if self._state == "idle": # если лунка ещё не нажата -убираем hover. hover нужен только для подсветки кликабельных лунок, если лунка уже нажата -подсветка не нужна
            self._hover = False
            self._draw()   # Перерисовываем с обычной обводкой HOLE_RIM. HOLE_RIM -тёмно-синяя обводка обычного состояния.
 
    def _click(self, _): # создаём метод для обработки события клика по холсту
        # Клик по холсту.
        if self._state == "idle" and self._on_click: #self._state == "idle" -лунка не была нажата раньше
            # Это предотвращает повторный клик по уже нажатой лунке
            # self._on_click -функция существует (не None)
            self._on_click(self._row, self._col) # Вызываем Game.hit(row, col) с координатами этой лунки
 
    def set_state(self, state): # Метод для изменения состояния лунки. Вызывается из Game.hit() после определения попадания или промаха.
        # Публичный метод -вызывается из Game.hit() чтобы сменить вид лунки. можно вызывать снаружи класса.
        self._state = state # Меняем состояние: "hit" или "miss"
        self._hover = False # Сбрасываем hover (лунка нажата - hover не нужен)
        self._draw() # Перерисовываем с новым состоянием
 
 
