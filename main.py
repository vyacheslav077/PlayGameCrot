    # ЗАГРУЗКА УРОВНЯ
 
    def load_level(self):
 
        for w in self.field.winfo_children():
            w.destroy()
        # Удаляем все лунки предыдущего уровня.
        # При первом вызове field пустой -цикл просто не выполнится.
        # При переходе на следующий уровень -удаляет старую сетку.
 
        rows, cols = self.LEVELS[self.level]
        # Распаковка кортежа. Пример: self.level=1, LEVELS[1]=(2,2), rows=2, cols=2.
        # Python позволяет: a, b = (1, 2) = a=1, b=2.
 
        self.mole = (random.randint(0, rows-1), random.randint(0, cols-1)) # Случайная позиция крота. random.randint(a, b) включает оба конца.
        # Для 2×2 сетки: строка 0 или 1, столбец 0 или 1.
        # Результат кортеж: например (1, 0) -вторая строка, первый столбец.
 
        self._update_hud()
        # Обновляем счётчики уровня и жизней в верхней панели.
 
        self.lbl_status.config( #lbl_status — это Label с текстом статуса ("Попадание!", "Промах!" и т.д.).config() — меняет параметры уже созданного виджета:
            text=f"Уровень {self.level+1} из {len(self.LEVELS)} -найди крота!", fg=SUBTEXT)
        # self.level -индекс (0, 1, 2), поэтому +1 для отображения (1, 2, 3).
        # len(self.LEVELS) = 3 — общее число уровней.
 
        log(f"Уровень {self.level+1} | Крот {self.mole} | {rows}x{cols}")
        # В лог пишем позицию крота -для отладки и анализа игр.
 
        self.buttons = [] # Создаём пустой список для хранения ссылок на лунки. Нужен чтобы потом обращаться к конкретной лунке: self.buttons[r][c]
 
        for i in range(rows):
            row_btns = []
            for j in range(cols): # Двойной цикл: i = строка (0..rows-1), j = столбец (0..cols-1). Для 3×3: создаёт 9 лунок -все комбинации (0,0),(0,1)...(2,2).
                btn = HoleButton(self.field, i, j, self.hit)
                # Создаём лунку. Передаём:
                # self.field -фрейм-контейнер.
                # i, j -позиция, будет передана в self.hit при клике.
                # self.hit -функция-обработчик клика.
 
                btn.grid(row=i, column=j, padx=12, pady=12) # grid() -менеджер геометрии для табличной раскладки.
                # padx=12, pady=12 -тступы между лунками (12px с каждой стороны).

                row_btns.append(btn) # Добавляем лунку в список текущей строки.
 
            self.buttons.append(row_btns)
            # Добавляем строку в двумерный список.
            # После цикла self.buttons[0][1] -лунка в строке 0, столбце 1.
 
 
    def _update_hud(self): # Обновляет отображение уровня и жизней в верхней панели. Вызывается: при загрузке уровня и при каждом промахе.
 
        self.lbl_level.config(text=f"Уровень {self.level+1} / {len(self.LEVELS)}")  # config() -меняет параметры существующего виджета. Аналог: создать новый Label. Но config() быстрее — не пересоздаёт виджет.
 
        h = "♥ " * self.lives + "♡ " * (self.MAX_LIVES - self.lives) # Строим строку сердечек: полные  + пустые. При lives=4, MAX_LIVES=6: "4 заполены а 2 нет"
 
        self.lbl_lives.config(text=h.strip()) # .strip() убирает пробел в конце строки.
 
    # ОБРАБОТКА КЛИКА ПО ЛУНКЕ
 
    def hit(self, r, c):  # Вызывается из HoleButton._click() когда игрок кликнул по лунке (r, c). r = row (строка), c = col (столбец) - индексы нажатой лунки.
        #Вызывается при клике мышью. Проверяет что лунка ещё не нажата (idle) — защита от двойного клика. Затем вызывает Game.hit(row, col) передавая координаты этой лунки.
        log(f"{self.player} кликнул ({r},{c})")
 
        if (r, c) == self.mole: # Сравниваем кортежи. (1, 2) == (1, 2) => True. ПОПАДАНИЕ: кликнутая лунка = позиция крота.
 
            self.buttons[r][c].set_state("hit") # Меняем вид нажатой лунки на зелёную (W).
 
            self.lbl_status.config(text="✓ Попадание! Молодец!", fg=GREEN)
 
            self.level += 1 # Переходим на следующий уровень.
 
            if self.level < len(self.LEVELS): # Если уровни ещё есть (level=1 < 3 => True):
                self.game_win.after(800, self.load_level) # Через 800мс загружаем следующий уровень. Задержка нужна чтобы игрок увидел зелёную лунку перед сменой.
            else:
                # level=3, len=3 => 3 < 3 = False => все уровни пройдены.
                self.game_win.after(900, self.win) # Через 900мс показываем экран победы.
 
        else: # ПРОМАХ: кликнутая лунка НЕ равна позиции крота
 
            self.buttons[r][c].set_state("miss") # Делаем нажатую лунку красной (X)
            self.lives -= 1 # Уменьшаем жизни на 1.
 
            self._update_hud() # Обновляем сердечки в HUD. HUD -верхняя панель с информацией об игроке, уровне и жизнях.
 
            self.lbl_status.config(
                text=f"✗ Промах! Жизней осталось: {self.lives}", fg=RED)
 
            if self.lives <= 0: # Жизни кончились — поражение.
 
                mr, mc = self.mole # Распаковываем позицию крота: mr=строка, mc=столбец.
 
                self.buttons[mr][mc].set_state("hit")
                # Показываем где был крот — подсвечиваем зелёным.
                # Игрок видит что пропустил перед экраном поражения.
 
                self.game_win.after(900, self.lose) # Через 900мс показываем экран поражения. Задержка: чтобы игрок увидел где был крот.

 
    # ЭКРАН РЕЗУЛЬТАТА (общий для победы и поражения)
 
    def _show_result(self, result): # result = "ПОБЕДА" или "ПОРАЖЕНИЕ"
 
        seconds = int(time.time() - self.start_time) # Время игры = текущее время - время старта. # int() округляет вниз: 34.7 => 34. Дробные секунды не нужны.
 
        save_result(self.player, result, self.lives, self.level, seconds) # Сохраняем итог в logs/results.txt.
 
        won   = result == "ПОБЕДА"   # True или False
        color = GREEN if won else RED #если победа => зелёный, иначе = красный.
 
        icon  = "★" if won else "✖" # Символ юникода: звезда или крест.
 
        if self.game_win and self.game_win.winfo_exists():
            self.game_win.destroy()
        self.game_win = None
        # Закрываем игровое окно перед показом результата
        # Без этого оба окна были бы открыты одновременно
 
        self.result_win = tk.Toplevel(self.root)
        self.result_win.title("Итог")
        self.result_win.configure(bg=BG)
        self.result_win.resizable(False, False) # Запрещаем менять размер окна результата, чтобы не ломать вёрстку.
        self.result_win.geometry("400x500")
 
        self.result_win.protocol("WM_DELETE_WINDOW", self._close_result_to_menu) # Крестик окна => вернуться в меню (не просто закрыть). WM_DELETE_WINDOW -событие нажатия на крестик, protocol -перехват этого события, _close_result_to_menu -метод для корректного закрытия окна результата и возврата в меню
 
        tk.Label(self.result_win, text=icon, # text=icon -звезда или крест в зависимости от результата. pack(pady=(28, 2)) -отступ 28px сверху и 2px снизу
                 font=("Segoe UI", 52, "bold"), fg=color, bg=BG).pack(pady=(28, 2))
 
        tk.Label(self.result_win, text=result,
                 font=("Segoe UI", 28, "bold"), fg=color, bg=BG).pack()
        # "ПОБЕДА" или "ПОРАЖЕНИЕ" под иконкой.
 
        self._sep(self.result_win, color=ACCENT, padx=50, pady=14) #self._sep -метод для создания линии-разделителя. color=ACCENT -фиолетовый цвет. padx=50 -отступы по бокам, pady=14 -отступы сверху/снизу.
 
        stats = tk.Frame(self.result_win, bg=PANEL, padx=24, pady=18)
        stats.pack(padx=40, fill="x")
        # Тёмный блок для статистики. padx=24, pady=18 — внутренние отступы Frame.
 
        def stat_row(label, val, vc=TEXT): # TEXT это цвет, задан в самом начале
            # Вспомогательная функция ВНУТРИ метода.
            # Создаёт одну строку статистики: "Игрок    Слава"
            # label - название (слева), val - значение (справа)
            f = tk.Frame(stats, bg=PANEL)
            f.pack(fill="x", pady=3)
            # Строка-контейнер
 
            tk.Label(f, text=label, font=("Segoe UI", 11),
                     fg=SUBTEXT, bg=PANEL, anchor="w").pack(side="left")
            # Название: серый текст, выровнен по левому краю
 
            tk.Label(f, text=val, font=("Segoe UI", 11, "bold"),
                     fg=vc, bg=PANEL, anchor="e").pack(side="right")
            # Значение: жирный, выровнен по правому краю
            # side="left" + side="right" в одном Frame => имитация двух колонок
 
        stat_row("Игрок",            self.player,                        ACCENT2)
        stat_row("Пройдено уровней", f"{self.level}/{len(self.LEVELS)}", GOLD)
 
        hearts = "♥" * self.lives + "♡" * (self.MAX_LIVES - self.lives)
        # Аналог _update_hud но без пробелов между сердечками (стиль результата)
        stat_row("Жизней осталось", hearts, RED)
        stat_row("Время игры", f"{seconds} сек", TEXT)
 
        self._sep(self.result_win, color=BTN_BG, padx=50, pady=10)
        # Тёмный разделитель перед кнопками
 
        StyledButton(self.result_win, "↺   Играть снова", self.play_again,
                     bg=GREEN, hover="#059669", fg=TEXT,
                     font_size=13, bold=True,
                     pad_x=30, pad_y=12).pack(fill="x", padx=40, pady=(4, 4))
        # Зелёная кнопка "Играть снова". hover="#059669" — тёмнее GREEN при наведении
        #  символ юникода "круговая стрелка" (refresh)
 
        StyledButton(self.result_win, "Главное меню", self._close_result_to_menu,
                     bg=ACCENT, hover="#6D28D9", fg=TEXT,
                     font_size=13, bold=True,
                     pad_x=30, pad_y=12).pack(fill="x", padx=40, pady=(0, 4))
        # Фиолетовая кнопка возврата в меню
 
 
    def _close_result_to_menu(self):
        # Вызывается из кнопки "Главное меню" и крестика окна результата
        if self.result_win and self.result_win.winfo_exists():
            self.result_win.destroy()
        self.result_win = None
        self.menu()
        # Очищаем ссылку и перестраиваем главное меню
 
 
    def win(self):
        log(f"ПОБЕДА | {self.player}")
        self._show_result("ПОБЕДА")
 
    def lose(self):
        log(f"ПОРАЖЕНИЕ | {self.player}")
        self._show_result("ПОРАЖЕНИЕ")
    # win() и lose() — тонкие обёртки: записывают в лог и вызывают _show_result
    # _show_result принимает строку — можно передать любой результат
 
 

    # ВОЗВРАТ В МЕНЮ ИЗ ИГРОВОГО ОКНА

    def back(self):
        # Вызывается: кнопка "В меню" в игровом окне, крестик игрового окна
 
        if self.game_win and self.game_win.winfo_exists():
            self.game_win.destroy()
        # Закрываем игровое окно если оно существует
        # Проверка нужна: back() мог быть вызван когда окно уже закрыто
 
        self.game_win = None
        # Обнуляем ссылку
 
        self.menu()
        # Перестраиваем главный экран меню
 
 
